     private DefaultTableModel montarTabela(){
        String[] colunas = {"ID", "Cliente", "CPF", "Data Nascimento", "Telefone", "Endereço", "Categoria", "Débito"};
        
        //Criamos a tabela "na mão"
        DefaultTableModel tabela = new DefaultTableModel(colunas, 0);
        
        //Pegar os dados da lista de produtos
        ArrayList<Cliente> lista = ListaCliente.listar();
        
        //FOR para MONTAR A LINHA na tabela
        for (int i = 0; i < lista.size(); i++) {
            Cliente c = lista.get(i);
            
            
            
            String [] linha = {
                Integer.toString(c.getId()),
                c.getNome(),
                c.getCpf(),
                c.getDataNasc(),
                c.getTelefone(),
                c.getEndereco(),
                c.getCategoria(),
                Double.toString(c.getPreco()),
                };
            tabela.addRow(linha); 
        }
        
        return tabela;
    }
    

    
    private int getPosicao(){
        int posicao = tabelaCliente.getSelectedRow();
        
        if(posicao <= -1) {
            JOptionPane.showMessageDialog(null, "Selecione um item para excluir:");
        }
        
        return posicao;
    }

