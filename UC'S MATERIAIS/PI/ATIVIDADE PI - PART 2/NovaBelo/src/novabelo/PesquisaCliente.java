    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String nomeProcurado = fieldNomeCliente();
        try {
            boolean clienteEncontrado = false;

            for (Cliente c : ListaCliente.listar()) {
                if (c.getNome().equalsIgnoreCase(nomeProcurado)) {
                    clienteEncontrado = true;
                     
                        
                        feedbackNomeCliente.setText("Nome do Cliente: " + c.getNome());
                        feedbackCpf.setText("CPF: " + c.getCpf());
                        feedbackNasc.setText("Data de Nasc: " + c.getDataNasc());
                        feedbackEndereco.setText("Endereco: " + c.getEndereco());
                        feedbackTelefone.setText("Telefone: " + c.getTelefone());
                        feedbackCategoria.setText("Categoria: " + c.getCategoria());
                        feedbackValor.setText("Debito: " + c.getPreco());
                    
                }
            }

            if (!clienteEncontrado) {
                JOptionPane.showMessageDialog(null, "Cliente n√£o encontrado.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao procurar o cliente");

        }
    }

